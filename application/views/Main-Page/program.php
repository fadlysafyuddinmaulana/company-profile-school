    <h3>PROGRAM</h3>
    <p class="justified"><strong>KURIKULUM PAUD</strong> menitikberatkan pada Implementasi Kurikulum Merdeka (IKM) yang mencakup tiga pilihan utama: Merdeka Belajar, Merdeka Berubah, dan Merdeka Berbagi. Kurikulum ini dirancang untuk meningkatkan kualitas pendidikan anak usia dini dengan pendekatan yang lebih fleksibel dan adaptif terhadap kebutuhan anak-anak.</p>
    <p class="justified"><strong>METODE PENGAJARAN</strong></p>
    <p class="justified">
      1. Pembelajaran Aktif : Mendorong siswa untuk terlibat secara aktif dalam proses belajar melalui diskusi, proyek, dan kegiatan kolaboratif.</p>
    <p class="justified">
      2. Pembelajaran Berbasis Proyek : Menggunakan proyek nyata untuk membantu siswa memahami konsep dan mengembangkan keterampilan berpikir kritis.</p>
    <p class="justified">
      3. Pembelajaran Kontekstual : Materi pembelajaran disesuaikan dengan konteks lokal dan kebutuhan siswa, sehingga lebih relevan dan bermakna</p>
    <p class="justified">
      4. Pengembangan Kompetensi : Fokus pada pengembangan berbagai kompetensi, termasuk literasi, numerasi, keterampilan sosial, dan keterampilan berpikir kritis.</p>
    <p class="justified">
      5. Pembelajaran Mendalam (Deep Learning) : Menekankan pada penciptaan suasana belajar yang berkesadaran, bermakna, dan menggembirakan melalui olah pikir, olah hati, olah rasa, dan olah raga secara holistik dan terpadu</p>
    <p class="justified"><strong>KEGIATAN HARIAN:</strong>
    <p class="justified">
      1. Pembelajaran Tematik : Menggunakan tema tertentu untuk mengajarkan berbagai konsep kepada anak-anak, seperti alam, keluarga atau profesi.</p>
    <p class="justified">
      2. Kegiatan kreatif : Melibatkan anak-anak dalam kegiatan seni dan kerajinan, seperti menggambar, mewarnai dan membuat kerajinan tangan.</p>
    <p class="justified">
      3. Kegiatan literasi : Membaca cerita, mengenalkan huruf dan angka, serta mengaji.</p>
